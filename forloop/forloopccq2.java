/*
 * Print even number between 1 to 10 using for loop and if else.
 */

 class forloopccq2
 {
    public static void main(String[] args)
    {
        for(int i=1;i<=10;i=i+1)
        {
            //print only even number
            if(i%2==0)
            {
                System.out.println("Even number:"+i);
            }
    }
 }
}